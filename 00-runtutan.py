# Program penghitung rata rata dari 3 nilai
# Input : Nilai (0 <= Nilai <= 100) sebanyak 3 buah
# Output : Rata rata

print("Masukan nilai 1 :")
nilai1 = float(input())

print("Masukan nilai 2 :")
nilai2 = float(input())

print("Masukan nilai 3 :")
nilai3 = float(input())

rata = (nilai1 + nilai2 + nilai3) / 3

print("Rata rata : ", rata)

